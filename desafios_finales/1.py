num1 = int(input('Ingrese un numero '))
num2 = int (input('ingrese un numero '))
operacion = input('ingrese que operacion quiere realizar')
if operacion == '*':
    resultadom = num1 * num2
    print(resultadom)
elif operacion == '/':
    resultadod = num1 / num2
    print(resultadod)
elif operacion == '+':
    resultados = num1 + num2
    print(resultados)
elif operacion == '-':
    resultador = num1 - num2
    print(resultador)
    
